const estacion = "Invierno";
switch (estacion) {
    case "Verano":
        console.log("Es verano");
        break;
    case "Otoño":
        console.log("Es otoño");
        break;
    case "Invierno":
        console.log("Es invierno");
        break;
    case "Primavera":
        console.log("Es primavera");
        break;
    default:
        console.log("Esa no es una estación")

}